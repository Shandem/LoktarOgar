int	ft_atoi(char *str)
{
	int		i;
	int		res;
	int		neg;

	i = 0;
	res = 0;
	neg = 1;
	while (str[i] != '\0' && ((str[i] >= '0' && str[i] <= '9') ||
						(str[i] == '-' || str[i] == '+')))
	{
		if (str[i] == '-')
				neg = -1;
		if (str[i] >= '0' && str[i] <= '9')
			res = (res * 10) + (str[i] - 48);
		i++;
	}
	while (str[i])
	return (res * neg);
}

