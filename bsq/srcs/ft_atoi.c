int		ft_atoi(char *str)
{
	int	result;
	int neg;
	int i;

	i = 0;
	result = 0;
	neg = 1;
	while (str[i] != '\0')
	{
		if (str[i] == '-')
			neg = -1;
		else if (str[i] >= '0' && str[i] <= '9')
			result = (result * 10) + (str[i] - 48);
		i++;
	}
	return (result * neg);
}
