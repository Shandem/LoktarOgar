#include <unistd.h>

int		main(int ac, char **av)
{
	int i;
	int flip;

	i = 0;
	if (ac == 2)
	{
		while (av[1][i])
		{
			flip = av[1][i];
			if (av[1][i] >= 'A' && av[1][i] <= 'Z')
				flip = 'Z' - av[1][i] + 'A';
			if (av[1][i] >= 'a' && av[1][i] <= 'z')
				flip = 'z' - av[1][i] + 'a';
			write(1, &flip, 1);
			i++;
		}
	}
	write(1, "\n", 1);
	return (0);
}
