int	*ftmap(int *tab, int length, int (*f)(int))
{
	int	i;
	int	*p;

	i = 0;
	p = (int*)malloc(sizeof(int) * (length));
	while (i < length)
	{
		p[i] = (*f)(tab[i]);
		++i;
	}
	return (p);
}
