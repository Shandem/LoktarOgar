int	ft_atoi(char *str)
{
	int	s;
	int	i;
	int	res;
	int	neg;

	s = 0;
	while (str[s] != '\0' && (str[s] == ' ' || str[s] == '\t' ||
			str[s] == '\r' || str[s] == '\n'))
		s++;
	i = s;
	res = 0;
	neg = 0;
	while (str[i] != '\0' && ((str[i] >= '0' && str[i] <= '9') ||
				(i == s && (str[i] == '-' || str[i] == '+'))))
	{
		if (str[i] == '-')
			neg = 1;
		if (str[i] >= '0' && str[i] <= '9')
			res = (res * 10) + (str[i] - 48);
		++i;
	}
	if (neg)
		res *= -1;
	return (res);
}
